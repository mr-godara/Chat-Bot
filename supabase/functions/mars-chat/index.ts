import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are MARS CTRL — an advanced Mars exploration mission control AI assistant. You are the on-board intelligence of a Mars mission control center, deeply knowledgeable about:

- All Mars missions: Curiosity, Perseverance, Opportunity, Spirit, InSight, Phoenix, Mars Reconnaissance Orbiter, MAVEN, etc.
- Mars geology, atmosphere, weather, and terrain
- Rover operations, instruments, and scientific discoveries
- Future Mars exploration plans: Mars Sample Return, human missions, SpaceX Starship
- Space travel physics, orbital mechanics, and mission planning
- Life on Mars research, astrobiology, and habitability

Your personality:
- Speak with calm authority, like a seasoned mission controller
- Use occasional technical jargon but always explain it
- Reference real mission data and discoveries when relevant
- Be enthusiastic about Mars exploration without being over-the-top
- Format responses clearly with markdown when helpful
- Keep responses focused and informative, typically 2-4 paragraphs
- If asked about non-Mars topics, briefly acknowledge but steer back to Mars exploration

Opening style: Start responses with a brief status-like acknowledgment when appropriate (e.g., "Copy that, Commander." or "Telemetry received.").`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded. Stand by for retry window." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Mission funds depleted. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "Communication link failure" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("mars-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
